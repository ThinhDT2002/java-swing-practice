private void initWebcam() {
        Dimension size = WebcamResolution.QVGA.getSize();
        webcam = Webcam.getWebcams().get(0); //0 is default webcam
        webcam.setViewSize(size);

        panel = new WebcamPanel(webcam);
        panel.setPreferredSize(size);
        panel.setFPSDisplayed(true);

       jPanel2.add(panel, new org.netbeans.lib.awtextra.AbsoluteConstraints(0, 0, 664, 500));

        executor.execute(this);
    }

    @Override
    public void run() {
        do {
            try {
                Thread.sleep(100);
            } catch (InterruptedException e) {
                e.printStackTrace();
            }

            Result result = null;
            BufferedImage image = null;

            if (webcam.isOpen()) {
                if ((image = webcam.getImage()) == null) {
                    continue;
                }
            }

            LuminanceSource source = new BufferedImageLuminanceSource(image);
            BinaryBitmap bitmap = new BinaryBitmap(new HybridBinarizer(source));

            try {
                result = new MultiFormatReader().decode(bitmap);
            } catch (NotFoundException e) {
                //No result...
            }

            if (result != null) {
                try {
                    String ma = result.getText();
                    NhanVien nhanVien = dao.selectById(ma);
                    if (nhanVien != null) {
                        Authorize.user = nhanVien;
                        new FormGiaoDienTong().setVisible(true);
                        this.setVisible(false);
                        webcam.close();
                        
                    } else {
                        MsgBox.alert(this, "Mã không hợp lệ!");
                    }
                } catch (Exception e) {
                }
            }
        } while (true);
    }

